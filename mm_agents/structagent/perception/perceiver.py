"""Perceiver — subgoal-conditioned multimodal observation distillation.

Replaces (raw screenshot + raw a11y) for the planner with a focused
``SubgoalSnapshot`` produced by a single VLM call per step.

Module layout:
  - dataclasses                    — SubgoalSnapshot + sub-types
  - renderer                       — to_prompt_block() for planner
  - perceive()                     — VLM call + parser + crop
  - _crop_screenshot               — given bbox, crop image bytes

See docs/PROGRESS_LEDGER_V2_CHANGELOG.md §17 for design context.
"""
from __future__ import annotations

import base64
import io
import json
import logging
import re
from dataclasses import dataclass, field
from typing import Any, Callable, Dict, List, Literal, Optional, Tuple

logger = logging.getLogger(__name__)


# --------------------------------------------------------------------------- #
# Data structures
# --------------------------------------------------------------------------- #

VerdictLiteral = Literal["yes", "no", "partial", "unknown"]
OverallVerdict = Literal["yes", "no", "partial"]
Coverage = Literal["complete", "partial"]


@dataclass
class ClauseCheck:
    """One sub-claim from the planner's expected_post_state, verified against
    the current screen by the perceiver.

    Each clause must carry concrete evidence (an a11y row quote, a bbox, or a
    URL substring) — perceiver is forbidden to hand-wave.
    """
    clause: str                 # auto-split from expected_post_state
    verdict: VerdictLiteral
    evidence: str               # a11y row / bbox / URL substring


@dataclass
class SubgoalCheck:
    """Turn-level verdict on the previous action's expected_post_state.

    Distinct from KeyNode's outcome state (task-level). This answers: did the
    very last planner subgoal achieve what it said it would?
    """
    overall: OverallVerdict
    clauses: List[ClauseCheck]


@dataclass
class Control:
    """One subgoal-relevant interactable element on the current screen.

    Perceiver picks ≤ 8 from the ~100-row prefiltered a11y candidate pool.
    Every entry must trace back to either an a11y row OR a vision_ocr bbox —
    no hallucinated controls.
    """
    label: str                                  # "Submit", "Jun 5", "Search bar (carl)"
    role: str                                   # "button", "textbox", "checkbox"
    state: List[str] = field(default_factory=list)
    # ["enabled"], ["checked", "focused"], ["disabled"], etc.
    value: Optional[str] = None                 # dropdown selected value, input current value
    bbox: Optional[Tuple[int, int, int, int]] = None  # (x1, y1, x2, y2) screen coords


@dataclass
class VisualFocus:
    """Mandatory visual companion to the text snapshot.

    A11y and vision must always double-anchor each other — each modality
    has independent failure modes, so the planner sees both every step.
    The crop is sized to encompass the relevant_controls bboxes (with
    padding), or falls back to the full screenshot when controls span
    too much of the screen.
    """
    bbox: Tuple[int, int, int, int]             # crop region in original screen coords
    crop_b64: str                               # base64-encoded PNG
    contains_controls: List[int]                # indices into relevant_controls inside crop
    out_of_crop_count: int                      # # of relevant_controls OUTSIDE the crop


@dataclass
class SlotBinding:
    """One unbound ledger slot the perceiver was able to resolve this step.

    Emitted by the perceiver only when the value is verbatim-visible in the
    current screenshot / a11y; perceiver MUST NOT guess or paraphrase. The
    planner promotes these into ``ledger.slots`` (bind-once) so downstream
    verify specs that reference ``${name}`` start running against a
    concrete value instead of short-circuiting to LLM judge.
    """
    name: str                # slot name (must exist in ledger.slots)
    value: str               # extracted verbatim from evidence
    evidence: str            # a11y row / bbox / quoted UI text justifying the value


@dataclass
class SubgoalSnapshot:
    """Replaces (raw screenshot + raw a11y) for the planner.

    Generated by one perceiver VLM call per step. ~600 tokens text + ~10-20KB
    crop image. Outcome state still flows through KeyNode → ledger; the
    snapshot here only carries perceiver-only fields (turn-level subgoal
    check, candidate controls, visual evidence, semantic transition,
    blockers, self-coverage assessment).
    """
    # Echo of the perceiver's input — so planner sees the query the
    # snapshot was conditioned on (paper-trail when debugging).
    subgoal_text: str
    expected_post_state: str

    # Five perceiver-only fields.
    subgoal_check: SubgoalCheck
    relevant_controls: List[Control]
    visual_focus: VisualFocus                   # required (always populated)
    transition_summary: str                     # ≤ 3 NL lines
    unexpected_blockers: List[str]
    coverage: Coverage

    # Slot bindings resolved this step (empty list when no slots were
    # unbound, or when none became readable in the current frame).
    # Planner promotes these to ledger.slots after the perceiver call.
    slot_bindings: List[SlotBinding] = field(default_factory=list)


# --------------------------------------------------------------------------- #
# Renderer
# --------------------------------------------------------------------------- #

_VERDICT_GLYPH = {
    "yes": "✓",
    "no": "✗",
    "partial": "~",
    "unknown": "?",
}


def _render_clause(clause: ClauseCheck) -> str:
    glyph = _VERDICT_GLYPH.get(clause.verdict, "?")
    # one-line summary + indented evidence so the planner sees both
    return (
        f"  {glyph} \"{clause.clause}\"\n"
        f"      → {clause.evidence}"
    )


def _render_control(idx: int, c: Control, in_crop: bool) -> str:
    state_str = f"[{', '.join(c.state)}]" if c.state else ""
    value_str = f" = {c.value!r}" if c.value else ""
    bbox_str = f" bbox={c.bbox}" if c.bbox else ""
    crop_marker = "← in crop" if in_crop else "← OUTSIDE crop"
    return (
        f"  [{idx + 1}] {c.role} {c.label!r} {state_str}{value_str}"
        f"{bbox_str}  {crop_marker}"
    )


def to_prompt_block(snap: SubgoalSnapshot) -> str:
    """Render the snapshot as a single text block for the planner.

    The visual_focus crop image is appended separately to the planner
    message as an image_url part. This function only renders text.
    """
    in_crop_set = set(snap.visual_focus.contains_controls)

    lines: List[str] = []
    lines.append("═══════ SCREEN SNAPSHOT (perceiver-distilled, subgoal-conditioned) ═══════")
    lines.append("")
    lines.append(f"▶ Current subgoal: {snap.subgoal_text}")
    lines.append(f"▶ Expected post-state: {snap.expected_post_state}")
    lines.append("")

    # ── SECTION: subgoal check ──
    lines.append("─── DID LAST ACTION ACHIEVE EXPECTED STATE? ───")
    lines.append(f"Overall: {snap.subgoal_check.overall.upper()}")
    if snap.subgoal_check.clauses:
        for clause in snap.subgoal_check.clauses:
            lines.append(_render_clause(clause))
    else:
        lines.append("  (no clauses extracted)")
    lines.append("")

    # ── SECTION: relevant controls ──
    lines.append("─── RELEVANT CONTROLS ───")
    if snap.relevant_controls:
        for idx, c in enumerate(snap.relevant_controls):
            lines.append(_render_control(idx, c, idx in in_crop_set))
    else:
        lines.append("  (none picked by perceiver)")
    lines.append("")

    # ── SECTION: transition ──
    lines.append("─── TRANSITION SINCE LAST TURN ───")
    lines.append(snap.transition_summary or "(no significant change)")
    lines.append("")

    # ── SECTION: blockers ──
    lines.append("─── BLOCKERS ───")
    if snap.unexpected_blockers:
        for b in snap.unexpected_blockers:
            lines.append(f"  • {b}")
    else:
        lines.append("  (none)")
    lines.append("")

    # ── SECTION: visual focus reference ──
    vf = snap.visual_focus
    n_in = len(vf.contains_controls)
    n_total = n_in + vf.out_of_crop_count
    lines.append("─── VISUAL EVIDENCE ───")
    lines.append(
        f"[CROP IMAGE attached above] bbox={vf.bbox}; "
        f"{n_in}/{n_total} relevant_controls visible in crop"
    )
    lines.append("")

    # ── SECTION: meta ──
    lines.append(f"─── PERCEIVER COVERAGE ───")
    lines.append(snap.coverage)

    return "\n".join(lines)


