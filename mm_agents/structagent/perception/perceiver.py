def _extract_json_block(raw: str) -> Optional[str]:
    """Pull the JSON object from the response. Tries <json> tag, then
    fenced ```json blocks, then the outermost balanced {...}.
    """
    if not raw:
        return None
    # 1. <json>...</json> (preferred — perceiver prompt asks for this)
    m = re.search(r"<json>\s*(.*?)\s*</json>", raw, re.DOTALL | re.IGNORECASE)
    if m:
        return m.group(1).strip()
    # 2. ```json ... ``` fenced block
    m = re.search(r"```(?:json)?\s*(\{.*\})\s*```", raw, re.DOTALL | re.IGNORECASE)
    if m:
        return m.group(1).strip()
    # 3. Outermost balanced {...} (fallback)
    s = raw.strip()
    start = s.find("{")
    if start < 0:
        return None
    depth = 0
    for i, ch in enumerate(s[start:], start=start):
        if ch == "{":
            depth += 1
        elif ch == "}":
            depth -= 1
            if depth == 0:
                return s[start:i + 1]
    return None


def _coerce_bbox(value: Any) -> Optional[Tuple[int, int, int, int]]:
    """Accept [x1,y1,x2,y2] (any number type). Return tuple of ints or None."""
    if not isinstance(value, (list, tuple)) or len(value) != 4:
        return None
    try:
        return tuple(int(v) for v in value)
    except (TypeError, ValueError):
        return None


def _rescale_bbox(
    bbox: Optional[Tuple[int, int, int, int]],
    *,
    src_size: Tuple[int, int],     # (w, h) the model is reasoning in
    dst_size: Tuple[int, int],     # (w, h) the actual screenshot
) -> Optional[Tuple[int, int, int, int]]:
    """Map a bbox from ``src_size`` (Qwen-VL's resized space) to
    ``dst_size`` (real screenshot pixels). Returns None if input is None
    or the source dimensions are degenerate."""
    if bbox is None:
        return None
    sw, sh = src_size
    dw, dh = dst_size
    if sw <= 0 or sh <= 0:
        return bbox
    fx, fy = dw / float(sw), dh / float(sh)
    x1, y1, x2, y2 = bbox
    return (int(round(x1 * fx)), int(round(y1 * fy)),
            int(round(x2 * fx)), int(round(y2 * fy)))


def _parse_snapshot(
    raw_response: str,
    *,
    subgoal_text: str,
    expected_post_state: str,
    screen_size: Tuple[int, int],          # ORIGINAL screenshot dims
    model_view_size: Optional[Tuple[int, int]] = None,   # what the model saw post-resize
    screenshot_bytes: bytes,
) -> Optional[SubgoalSnapshot]:
    """Parse the perceiver's raw text response into a SubgoalSnapshot.

    Bbox values in the response are interpreted as pixel coordinates in
    ``model_view_size`` (Qwen-VL's smart_resize space) and rescaled to
    ``screen_size`` (the real screenshot) before populating the
    SubgoalSnapshot — so downstream consumers (crop, planner) see real
    screen coords throughout.

    Returns None on any structural failure — caller should fall back to
    raw observations in that case.
    """
    # Default: no rescale (model_view == screen) — used by unit tests
    # with hand-crafted JSON in real-screen pixel space.
    if model_view_size is None:
        model_view_size = screen_size

    json_blob = _extract_json_block(raw_response)
    if not json_blob:
        logger.warning("[perceiver] no JSON block found in response")
        return None
    try:
        data = json.loads(json_blob)
    except json.JSONDecodeError as e:
        logger.warning("[perceiver] JSON parse error: %s", e)
        return None

    if not isinstance(data, dict):
        logger.warning("[perceiver] JSON is not an object: %s", type(data))
        return None

    # ── subgoal_check ──
    sc_raw = data.get("subgoal_check") or {}
    overall = sc_raw.get("overall", "unknown")
    if overall not in ("yes", "no", "partial"):
        overall = "partial"  # safest default

    clauses_out: List[ClauseCheck] = []
    for c in (sc_raw.get("clauses") or []):
        if not isinstance(c, dict):
            continue
        verdict = c.get("verdict", "unknown")
        if verdict not in ("yes", "no", "partial", "unknown"):
            verdict = "unknown"
        clauses_out.append(ClauseCheck(
            clause=str(c.get("clause", ""))[:240],
            verdict=verdict,
            evidence=str(c.get("evidence", ""))[:240],
        ))
    subgoal_check = SubgoalCheck(overall=overall, clauses=clauses_out)

    # ── relevant_controls ──
    # Each bbox is rescaled from model-view space to real screen pixels
    # so the planner sees coords matching the actual screenshot.
    controls_out: List[Control] = []
    for c in (data.get("relevant_controls") or [])[:8]:
        if not isinstance(c, dict):
            continue
        raw_bbox = _coerce_bbox(c.get("bbox"))
        bbox = _rescale_bbox(raw_bbox,
                              src_size=model_view_size,
                              dst_size=screen_size)
        state = c.get("state") or []
        if not isinstance(state, list):
            state = []
        controls_out.append(Control(
            label=str(c.get("label", ""))[:120],
            role=str(c.get("role", ""))[:40],
            state=[str(s)[:30] for s in state],
            value=(str(c["value"])[:120] if c.get("value") is not None else None),
            bbox=bbox,
        ))

    # ── visual_focus ──
    vf_raw = data.get("visual_focus") or {}
    raw_vf_bbox = _coerce_bbox(vf_raw.get("bbox"))
    if raw_vf_bbox is None:
        # Required field missing — fall back to full screen (real coords)
        vf_bbox = (0, 0, screen_size[0], screen_size[1])
        logger.warning("[perceiver] visual_focus.bbox missing — using full screen")
    else:
        vf_bbox = _rescale_bbox(raw_vf_bbox,
                                 src_size=model_view_size,
                                 dst_size=screen_size)

    # NOTE: physical cropping disabled — Qwen-VL's bbox precision is too
    # low to crop reliably (off-target crops were a worse signal than the
    # full screenshot). The planner attaches the full current frame via
    # the K-frame current-attach in _pa_call_planner, so we just override
    # visual_focus.bbox to FULL SCREEN here for honest accounting.
    # The perceiver's predicted bbox (already rescaled above) is dropped.
    vf_bbox = (0, 0, screen_size[0], screen_size[1])
    crop_b64 = ""

    # Compute contains_controls + out_of_crop_count from bbox membership
    contains: List[int] = []
    out_count = 0
    for i, ctl in enumerate(controls_out):
        if ctl.bbox and _bbox_intersects(ctl.bbox, vf_bbox):
            contains.append(i)
        else:
            out_count += 1

    visual_focus = VisualFocus(
        bbox=vf_bbox,
        crop_b64=crop_b64,
        contains_controls=contains,
        out_of_crop_count=out_count,
    )

    # ── transition / blockers / coverage ──
    transition = str(data.get("transition_summary") or "")[:600]
    blockers_raw = data.get("unexpected_blockers") or []
    if not isinstance(blockers_raw, list):
        blockers_raw = []
    blockers = [str(b)[:240] for b in blockers_raw]
    coverage = data.get("coverage", "partial")
    if coverage not in ("complete", "partial"):
        coverage = "partial"

    # ── slot_bindings ──
    # Each entry must have a non-empty ``name`` and ``value``; malformed
    # entries are dropped silently. Dedup by name keeping the FIRST
    # occurrence (perceiver shouldn't emit dupes, but if it does, prefer
    # the first one since the second is likely a re-statement).
    slot_bindings_out: List[SlotBinding] = []
    seen_names: set = set()
    for sb in (data.get("slot_bindings") or []):
        if not isinstance(sb, dict):
            continue
        name = str(sb.get("name") or "").strip()
        value = sb.get("value")
        if value is None:
            continue
        value = str(value).strip()
        if not name or not value or name in seen_names:
            continue
        seen_names.add(name)
        slot_bindings_out.append(SlotBinding(
            name=name[:60],
            value=value[:512],
            evidence=str(sb.get("evidence") or "")[:240],
        ))

    return SubgoalSnapshot(
        subgoal_text=subgoal_text,
        expected_post_state=expected_post_state,
        subgoal_check=subgoal_check,
        relevant_controls=controls_out,
        visual_focus=visual_focus,
        transition_summary=transition,
        unexpected_blockers=blockers,
        coverage=coverage,
        slot_bindings=slot_bindings_out,
    )


def _bbox_intersects(a: Tuple[int, int, int, int],
                     b: Tuple[int, int, int, int]) -> bool:
    """Return True if rectangles a and b overlap (any pixel)."""
    ax1, ay1, ax2, ay2 = a
    bx1, by1, bx2, by2 = b
    return not (ax2 <= bx1 or bx2 <= ax1 or ay2 <= by1 or by2 <= ay1)


