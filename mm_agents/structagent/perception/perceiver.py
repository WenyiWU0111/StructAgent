# Image cropping helper
# --------------------------------------------------------------------------- #

def _crop_screenshot(
    screenshot_bytes: bytes,
    bbox: Tuple[int, int, int, int],
    max_long_edge: int = 800,
) -> Tuple[bytes, Tuple[int, int, int, int]]:
    """Crop the screenshot to the bbox and resize so the long edge ≤ max.

    Returns (png_bytes, sanitized_bbox).
    Sanitized bbox is clamped to image bounds.
    """
    try:
        from PIL import Image
    except ImportError as e:  # pragma: no cover
        raise RuntimeError("Pillow not installed") from e

    img = Image.open(io.BytesIO(screenshot_bytes))
    w, h = img.size

    x1, y1, x2, y2 = bbox
    # Sanitize: clamp to image, ensure x1<x2, y1<y2.
    x1 = max(0, min(int(x1), w - 1))
    y1 = max(0, min(int(y1), h - 1))
    x2 = max(x1 + 1, min(int(x2), w))
    y2 = max(y1 + 1, min(int(y2), h))

    crop = img.crop((x1, y1, x2, y2))

    # Resize so long edge ≤ max_long_edge to bound token cost.
    cw, ch = crop.size
    long_edge = max(cw, ch)
    if long_edge > max_long_edge:
        scale = max_long_edge / float(long_edge)
        nw, nh = int(cw * scale), int(ch * scale)
        crop = crop.resize((nw, nh), Image.LANCZOS)

    buf = io.BytesIO()
    crop.save(buf, format="PNG")
    return buf.getvalue(), (x1, y1, x2, y2)


