def perceive(
    *,
    screenshot_bytes: bytes,
    screen_size: Tuple[int, int],     # ORIGINAL screen pixel size (orig_w, orig_h)
    a11y_text: str,
    subgoal_text: str,
    expected_post_state: str,
    target_outcomes: List[Any],   # objects with .id, .evidence_hint
    last_action_summary: str,
    transition_block: str,        # already-rendered a11y delta or NL summary
    call_llm: Callable,
    model: Optional[str] = None,
    max_tokens: int = 1500,
    raw_sink: Optional[Callable[[str], None]] = None,   # capture raw response for debug
    unbound_slots: Optional[List[Any]] = None,   # ledger Slot objects (.name/.type/.description/.source_hint)
) -> Optional[SubgoalSnapshot]:
    """Run the perceiver: 1 LLM call, parse, return SubgoalSnapshot.

    Returns None on LLM failure or parse failure — caller should fall
    back to raw screenshot + raw a11y for the planner.
    """
    # Tiny helper local-scoped: prefix the a11y rows with the shared
    # state-column legend so the perceiver doesn't have to remember
    # 'enabled' vs 'selected' semantics from the system prompt.
    def _format_a11y_with_legend(rows: str, *, label: str) -> str:
        from mm_agents.structagent.ledger.support.a11y_legend import with_legend
        return with_legend(rows or "", header=f"{label} (tag\\ttext\\tstate):")

    # Compute the dimensions Qwen-VL will actually see post smart_resize.
    # We tell the perceiver this dimension as 'screen size' so its bbox
    # outputs are in the same space the model is reasoning in. Parser
    # rescales those bboxes back to ``screen_size`` (real screenshot).
    try:
        from mm_agents.structagent.utils.image import smart_resize
        model_view_h, model_view_w = smart_resize(
            screen_size[1], screen_size[0],
        )
    except Exception:
        # Fallback: use original size (no rescale)
        model_view_h, model_view_w = screen_size[1], screen_size[0]
    model_view_size = (int(model_view_w), int(model_view_h))

    user_text_parts: List[str] = []

    # Screen size so the perceiver knows the bbox space it should output in.
    # IMPORTANT: this is the model-view dimensions (post smart_resize),
    # NOT the original screenshot dims. The runtime rescales bboxes back.
    user_text_parts.append(
        f"Screen size (w x h): {model_view_size[0]} x {model_view_size[1]}"
    )

    # Subgoal context
    user_text_parts.append(f"Current subgoal:\n  {subgoal_text}")
    user_text_parts.append(f"Expected post-state:\n  {expected_post_state}")
    user_text_parts.append(f"Actor's last action: {last_action_summary}")

    # Active outcomes with evidence hints
    if target_outcomes:
        oc_lines = ["Active task outcomes (verify against these):"]
        for o in target_outcomes:
            oid = getattr(o, "id", "?")
            hint = getattr(o, "evidence_hint", "")
            oc_lines.append(f"  - {oid}: {hint}")
        user_text_parts.append("\n".join(oc_lines))

    # A11y rows (prefiltered)
    user_text_parts.append(
        _format_a11y_with_legend(a11y_text, label="A11y tree (subgoal-prefiltered)")
    )

    # Transition info (delta or NL summary from previous turn)
    if transition_block:
        user_text_parts.append(f"Transition since last turn:\n{transition_block}")

    # Unresolved ledger slots — only rendered when present, so slot-free
    # tasks pay zero prompt overhead. The system prompt's slot_bindings
    # contract activates only when this section is included.
    if unbound_slots:
        slot_lines = [
            "UNRESOLVED VALUES (ledger needs these to verify outcomes):",
        ]
        for s in unbound_slots:
            name = getattr(s, "name", "?")
            stype = getattr(s, "type", "text")
            desc = getattr(s, "description", "") or ""
            hint = getattr(s, "source_hint", "") or ""
            slot_lines.append(f"  - ${{{name}}} ({stype}): {desc}")
            if hint:
                slot_lines.append(f"      hint: {hint}")
        slot_lines.append(
            "For each slot whose CONCRETE value is verbatim-readable on the "
            "current screen / a11y, emit a slot_bindings entry. If only the "
            "referent is visible (e.g. link text without href, truncated "
            "value), OMIT — never guess."
        )
        user_text_parts.append("\n".join(slot_lines))

    # Build messages: image first, then text
    user_content: List[Dict[str, Any]] = []
    if screenshot_bytes:
        b64 = base64.b64encode(screenshot_bytes).decode("ascii")
        user_content.append({
            "type": "image_url",
            "image_url": {"url": f"data:image/png;base64,{b64}"},
        })
    user_content.append({"type": "text", "text": "\n\n".join(user_text_parts)})

    messages = [
        {"role": "system", "content": _PERCEIVER_SYSTEM_PROMPT},
        {"role": "user", "content": user_content},
    ]

    try:
        raw = call_llm(
            {"model": model, "messages": messages,
             "max_tokens": max_tokens, "temperature": 0.0, "top_p": 0.9},
            model,
        )
    except Exception as e:
        logger.warning("[perceiver] LLM call failed: %s", e)
        return None
    if not raw:
        logger.warning("[perceiver] LLM returned empty")
        return None

    # Notify raw-sink BEFORE parsing so debug dumps survive parse failures
    if raw_sink is not None:
        try:
            raw_sink(raw)
        except Exception as e:
            logger.info("[perceiver] raw_sink raised: %s", e)

    return _parse_snapshot(
        raw,
        subgoal_text=subgoal_text,
        expected_post_state=expected_post_state,
        screen_size=screen_size,
        model_view_size=model_view_size,
        screenshot_bytes=screenshot_bytes,
    )


# --------------------------------------------------------------------------- #
# Environment perceiver — once per task
# --------------------------------------------------------------------------- #
#
# The same digestion idea as ``perceive`` (which compresses the per-step
# a11y tree by subgoal context), applied to the VM's task-start
# environment probe (binaries, config files, GSettings schema pool,
# processes). It runs ONCE per task at init_ledger time and produces a
# small text block that the ledger initializer + planner consume.
#
# The probe itself (see ``probes.py``) is task-agnostic — it knows
# nothing about the user's instruction. THIS function is where the
# task-conditioned filtering happens, so the prompt downstream sees a
# focused subset rather than 400+ schemas plus 20 paths.

_ENVIRONMENT_PERCEIVER_PROMPT = """You are the ENVIRONMENT PERCEIVER. The
user has given a task instruction. You also see a raw probe of the VM
captured at task start: identity, available binaries, app config-file
locations, the full GNOME GSettings schema pool, and current-running
processes.

Your job is to compress the probe into a compact context block that
keeps ONLY the items potentially useful for completing this specific
task, and drops the irrelevant majority. The downstream ledger
initializer and planner will read your output to ground their
decisions (which schema to query, which binary to invoke, which file
to edit, …) — they will NOT see the raw probe.

Hard rules:
  • NEVER invent any identifier (schema, binary, path, process) that
    is NOT verbatim in the raw probe. Filter and rephrase only.
  • Recall > precision. When uncertain whether an item is relevant,
    KEEP it. Missing a relevant schema is worse than including an
    extra one.
  • Quote schemas / paths / binary names exactly as the probe spelled
    them.

Output exactly this plain-text shape (no JSON, no markdown headers):

  [Identity]
  user=<from probe>  home=<from probe>  os=<from probe NAME if shown>

  [Relevant binaries]   (omit section if none look task-relevant)
    <binary> -> <path>
    <binary> -> NOT_FOUND
    ...
  ── Standard POSIX tools (cp, mv, rm, mkdir, ls, find, sed, awk,
     grep, cat, echo, tr, sort, uniq, cut, wc, tee, tar, gzip, chmod,
     chown, etc.) are universally available on Ubuntu and MAY be used
     by the downstream agent even if a specific one isn't enumerated
     in the probe's binaries section. Do NOT mark them as missing or
     omit them from your reasoning. Mention them in this section ONLY
     if the task specifically revolves around one of them (e.g. the
     task is literally "use sed to ...").

  [Relevant config files]   (omit if none)
    <absolute path>  (existed: yes|no)

  [Possibly useful files]   (omit if none; user documents under
                             Desktop / Documents / Downloads the task
                             may need to open or edit)
    <absolute path>
    ...
  ── Source these ONLY from the probe's "### user dirs" listing; quote
     each absolute path VERBATIM (never invent or guess a path).
  ── Keep a file ONLY when its name plausibly matches THIS task's
     subject — the document/spreadsheet/archive the instruction refers
     to. DROP everything unrelated (silently — do not explain). If the
     probe listed many files and only one matches the task, emit only
     that one. If none match, omit the whole section.
  ── When ONE file's name is an exact / near-exact match for the
     document the instruction explicitly names, emit that file ALONE —
     do NOT also add files that merely share a single theme word with
     the task. List several only when the target is genuinely
     ambiguous.
  ── These files are NOT open yet. The downstream agent should open one
     directly with the ``open_app`` action — ``open_app(app=<app>,
     file=<absolute path>)`` — instead of hunting through the GUI file
     picker; that avoids mis-clicks and opening a blank document by
     mistake.

  [Relevant GSettings schemas]   (omit if the task has no system-setting flavor)
    <schema-1>
      keys: <only the keys whose name suggests they back this task — comma-separated>
    <schema-2>
      keys: <subset of schema-2's keys relevant to this task>
    ...
  ── If the probe's `gsettings list-keys` command line for a schema
     ended with a `:<dconf-path>` suffix (i.e. the schema is
     relocatable — defined statically but addressed by a runtime
     dconf path), include that `:<dconf-path>` suffix verbatim as
     part of the schema name in your output. Otherwise `gsettings
     get/set` fails with "Schema ... is relocatable (path must be
     specified)". Copy the suffix EXACTLY as it appeared in the probe.
  ── For each task-relevant schema, list ONLY the keys (from the probe's
     list-keys section) that plausibly back the task. Drop keys whose
     name is clearly unrelated. If the probe shows a schema HAS no
     plausibly-relevant key, DROP that schema entirely — including a
     schema with no usable key just misleads the downstream LLM.
  ── HARD RULE: if the probe's list-keys section did NOT enumerate keys
     for a schema (you can see no "schema=<name>" block for it under
     "### gsettings keys"), you MUST write `keys: (probe did not list)`
     — NEVER invent keys from training memory. Hallucinated keys lead
     to verifier commands that always fail.
  ── Do NOT include schemas, binaries, or items as "dropped because ..."
     — if you are dropping it, simply OMIT it. Do not explain.

  [Relevant dconf paths]   (omit if none; only include when the probe's
                            "### dconf dump …" section contains a
                            line directly relevant to this task)
    <full dconf path, including any UUID-instanced segment>=<value-from-dump>
    <full dconf path>=<value-from-dump>
    ...
  ── Some settings (notably GNOME Terminal profiles) live at dconf
     paths that DO NOT appear in `gsettings list-schemas`. The dconf
     dump section is your only source for them. Quote the exact path
     (with any UUID) verbatim; the downstream LLM will need it to
     construct the `gsettings get/set <schema>:<dconf-path>` form.
  ── If the dconf dump section is EMPTY but a related UUID is visible
     elsewhere in the probe (e.g. the gnome-terminal probe section
     lists ``default-profile-uuid: 'xxx-yyy-zzz'``), write the path
     using that REAL UUID. If no UUID is visible anywhere in the
     probe, OMIT the entry — NEVER write `<UUID>` or any placeholder.

  [Running processes]   (only if the task is about a running app)
    <line from pgrep>

Keep total output ≤ 1500 characters. End with a single empty line."""


# Domain-conditional perceiver schema. Appended to
# :data:`_ENVIRONMENT_PERCEIVER_PROMPT` only when the task's domain
# matches. These blocks define an ADDITIONAL output section and route
# domain entries into it — the WHY (storage conventions, allowed
# verify shapes, …) lives in the corresponding domain_knowledge file
# and is injected into the planner / init_ledger separately.
_VLC_PERCEIVER_RULES = """

  [VLC config]   (this task's domain is VLC; emit when the probe has a
                  `### vlc config` section)
    path: <path-from-probe>  (<exists | not yet present>)
    <key>=<value>   ← copy each task-relevant active line VERBATIM from
                      the probe's "active key=value lines" block; if no
                      lines are listed, emit only the path line.

  ── All VLC-related items go in [VLC config]. Do NOT route VLC entries
     into [Relevant GSettings schemas] or [Relevant dconf paths]."""


_LIBREOFFICE_PERCEIVER_RULES = """

  [LibreOffice / Python office libs]   (this task's domain is one of
                                         libreoffice_writer / impress /
                                         calc; emit when the probe has
                                         a `### libreoffice` section)
    soffice version: <version line from probe, or "not installed">
    python-docx:     <version, or "not available">
    openpyxl:        <version, or "not available">
    python-pptx:     <version, or "not available">

  ── These versions tell the downstream agent whether the python3 -c
     verify shape is usable for this task's document type. If
     python-docx is available, .docx tasks can use shell_command
     ["python3", "-c", "..."] verify specs and python-docx-driven
     cli_run edits. If not, fall back to a11y / GUI."""


_DOMAIN_PERCEIVER_RULES = {
    "vlc":                 _VLC_PERCEIVER_RULES,
    "libreoffice_writer":  _LIBREOFFICE_PERCEIVER_RULES,
    "libreoffice_calc":    _LIBREOFFICE_PERCEIVER_RULES,
    "libreoffice_impress": _LIBREOFFICE_PERCEIVER_RULES,
}


def _build_perceiver_prompt(domain: Optional[str]) -> str:
    tail = _DOMAIN_PERCEIVER_RULES.get((domain or "").lower().strip(), "")
    return _ENVIRONMENT_PERCEIVER_PROMPT + tail if tail else _ENVIRONMENT_PERCEIVER_PROMPT


def perceive_environment(
    *,
    instruction: str,
    probe_text: str,
    call_llm,
    model: Optional[str] = None,
    domain: Optional[str] = None,
    max_tokens: int = 800,
) -> Optional[str]:
    """Once-per-task: filter the raw probe down to task-relevant items.

    Returns the digested context block (plain text, ≤ ~1.5KB) or ``None``
    on LLM failure / empty input. Caller decides whether to fall back to
    raw probe text or omit the section entirely.

    When ``domain`` matches a known per-domain block (currently
    ``"vlc"``), the block is appended to the system prompt so the
    perceiver knows the additional output section to emit. The full
    domain conventions are NOT repeated here — they ship in the
    corresponding domain_knowledge file injected into the planner.
    """
    if not probe_text or not probe_text.strip():
        return None
    system_prompt = _build_perceiver_prompt(domain)
    user_text = (
        f"Task instruction:\n{instruction.strip()}\n\n"
        f"Raw VM probe at task start:\n```\n{probe_text}\n```\n\n"
        "Output the compact context block now. Filter only — never invent."
    )
    payload = {
        "model": model,
        "messages": [
            {"role": "system", "content": system_prompt},
            {"role": "user", "content": user_text},
        ],
        "max_tokens": max_tokens,
        "temperature": 0.0,
        "top_p": 0.9,
    }
    try:
        out = call_llm(payload, model)
    except Exception as e:
        logger.warning("[env-perceiver] LLM call failed: %s", e)
        return None
    if not out or not out.strip():
        logger.info("[env-perceiver] empty output")
        return None
    return out.strip()
